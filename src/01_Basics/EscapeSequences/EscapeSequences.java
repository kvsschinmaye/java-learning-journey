public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("=== Java Escape Sequences Demonstration ===");

        // \n -> New Line
        System.out.println("\n1. New Line :");
        System.out.println("Hello\nWorld");

        // \t -> Horizontal Tab
        System.out.println("\n2. Tab Space :");
        System.out.println("Name\tRollNo\tBranch");

        // \b -> Backspace
        System.out.println("\n3. Backspace :");
        System.out.println("Helloo\b");

        // \r -> Carriage Return
        System.out.println("\n4. Carriage Return :");
        System.out.println("Hello\rJava");

        // \' -> Single Quote
        System.out.println("\n5. Single Quote :");
        System.out.println("Java is called \'Platform Independent\' language.");

        // \" -> Double Quote
        System.out.println("\n6. Double Quote :");
        System.out.println("\"Java Programming\"");

        // \\ -> Backslash
        System.out.println("\n7. Backslash :");
        System.out.println("C:\\Users\\Chinmaye");

    }
}